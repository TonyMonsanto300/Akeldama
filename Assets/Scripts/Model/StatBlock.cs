public class StatBlock {
    int strength;
    int dexterity;
    int mind;
    int agility;
    int spirit;
    int charisma;

    public StatBlock(int strength, int dexterity, int agility, int mind, int spirit, int charisma) {
        this.strength = strength;
        this.dexterity = dexterity;
        this.agility = agility;
        this.mind = mind;
        this.spirit = spirit;
        this.charisma = charisma;
    }
}